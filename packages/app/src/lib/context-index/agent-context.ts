// WRITE FUNCTIONS
// =============================================================================

/** Map of agent filenames to their corresponding emit-toggle flag in the context store. */
function enabledAgentFilenames(): string[] {
  const s = useAgentSettingsStore.getState();
  const out: string[] = [];
  if (s.emitClaudeMd) out.push(FILE_NAMES.CLAUDE_MD);
  if (s.emitAgentsMd) out.push(FILE_NAMES.AGENTS_MD);
  if (s.emitGeminiMd) out.push(FILE_NAMES.GEMINI_MD);
  return out;
}

/** All three filenames whose emit-toggle is OFF — these get deleted on write. */
function disabledAgentFilenames(): string[] {
  const s = useAgentSettingsStore.getState();
  const out: string[] = [];
  if (!s.emitClaudeMd) out.push(FILE_NAMES.CLAUDE_MD);
  if (!s.emitAgentsMd) out.push(FILE_NAMES.AGENTS_MD);
  if (!s.emitGeminiMd) out.push(FILE_NAMES.GEMINI_MD);
  return out;
}

/** Remove a file if it's present; a missing file is not an error. */
async function removeIfExists(path: string): Promise<void> {
  if (await getStorage().exists(path)) await getStorage().removeFile(path);
}

export async function writeTopLevelAgentFiles(workspaces: Workspace[]): Promise<void> {
  if (skipAgentFileWrite()) return;

  const deskPath = await getDeskPath();
  const enabled = enabledAgentFilenames();
  const disabled = disabledAgentFilenames();

  if (enabled.length > 0) {
    const content = buildTopLevelContext(workspaces, deskPath);
    for (const name of enabled) {
      await getStorage().writeTextFile(await joinPath(deskPath, name), content);
    }
  }

  // Sweep any files for toggles that are off — keeps disk in sync with settings.
  for (const name of disabled) {
    await removeIfExists(await joinPath(deskPath, name));
  }
}

export async function writePerWorkspaceAgentFiles(
  workspaceId: string,
  workspace: Workspace,
  projects: Project[]
): Promise<void> {
  if (skipAgentFileWrite()) return;

  const workspacePath = await getWorkspacePath(workspaceId);
  const enabled = enabledAgentFilenames();
  const disabled = disabledAgentFilenames();

  if (enabled.length > 0) {
    const content = buildPerWorkspaceContext(workspace, projects);
    for (const name of enabled) {
      await getStorage().writeTextFile(await joinPath(workspacePath, name), content);
    }
  }

  for (const name of disabled) {
    await removeIfExists(await joinPath(workspacePath, name));
  }
}

// Delete every generated agent file from the data folder; used when the catalog is cleared.
export async function deleteGeneratedAgentFiles(workspaces: Workspace[]): Promise<void> {
  if (skipAgentFileWrite()) return;

  const deskPath = await getDeskPath();
  for (const name of [FILE_NAMES.CLAUDE_MD, FILE_NAMES.AGENTS_MD, FILE_NAMES.GEMINI_MD]) {
    await removeIfExists(await joinPath(deskPath, name));
  }

  for (const ws of workspaces) {
    const workspacePath = await getWorkspacePath(ws.id);
    for (const name of [
      FILE_NAMES.CLAUDE_MD,
      FILE_NAMES.AGENTS_MD,
      FILE_NAMES.GEMINI_MD,
      FILE_NAMES.WORKSPACE_CONTEXT_MD,
    ]) {
      await removeIfExists(await joinPath(workspacePath, name));
    }
  }
}
